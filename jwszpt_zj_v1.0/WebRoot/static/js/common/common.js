//自动将form表单封装成json 
function formToJson(formObj){    
   var o = {};    
   var a = formObj.serializeArray();    
   $.each(a, function() {    
       if (o[this.name]) {    
           if (!o[this.name].push) {    
               o[this.name] = [o[this.name]];    
           }    
           o[this.name].push(this.value || '');    
       } else {    
           o[this.name] = this.value || '';    
       }    
   });    
   return JSON.stringify(o);    
}; 
